			<div class="page_title">The Zoological Survey of India</div>
			<p>The Zoological Survey of India (ZSI) is the premier taxonomic research organization in India. It was established on 1 July 1916 to promote surveys, exploration and research leading to advancement of our knowledge of various aspects of the exceptionally rich animal life of India. The ZSI had its genesis as the Zoological Section of the Indian Museum at Calcutta in 1875. Since its inception, the ZSI has been documenting the diversity and distribution of the fauna of India towards carrying out its mandate of conducting exploration-cum-taxonomic-research programmes. The ZSI has published an extremely large amount of information on all animal taxa, from Protozoa to Mammalia. This information is a boon to researchers, students, conservation managers and amateur naturalists.</p>
			<p>The ZSI has ushered in the era of digitisation with an ambitious archives project, encompassing all its published documents (more than 200,000 pages of printed material). This large body of information is now available as a text-searchable, easily accessible digital archive. All the volumes published by the ZSI have been digitised using state-of-the-art technology and software. These publications have been electronically restored and can be downloaded in the Portable Document Format (PDF) or viewed in the DjVu format (DjVu plugin should be installed for which the 'Get DjVu' link in the sidebar can be used. DjVu format is not supported in Google Chrome browser and hence 'Download (PDF)' links may be used.) They can be searched fully with different search filters. Text searches can be made across all publications or restricted by the user to any particular publication, year, author, taxon, etc.</p>
			<p>The digital archive of the publications of the ZSI represents a very valuable resource for research on Indian fauna.</p>
			<p><span class="bld">Records of the Indian Museum</span> and <span class="bld">Records of the Zoological Survey of India</span></p>
			<p>The Records of the Indian Museum served as a vehicle for publishing zoological research articles even before the establishment of the ZSI. At one time, it was the only journal for publishing taxonomic research papers, descriptions of new species, new records, revisionary studies, etc. for scientists of the ZSI. The first issue was published in 1907. After India’s independence, the ZSI published 88 issues of the Records of the Indian Museum in 21 volumes. After 1962, the journal was renamed the Records of the Zoological Survey of India.</p>
			<p>The Records of the Zoological Survey of India is a quarterly in-house journal now. It is a medium for zoological communications related to taxonomy, faunistics, biology, ecology and populations of all taxa. Since 1947, 168 issues of the Records of the Zoological Survey of India have been published in 67 volumes.</p>
			<p><span class="bld">Memoirs of Indian Museum</span> and <span class="bld">Memoirs of the Zoological survey of India</span></p>
			<p>The Memoirs are for publishing works on the systematics, phylogeny and biogeography of a group of animals or of groups of animals occurring in an ecologically defined area and any other work of a monographic nature. It is an occasional publication, brought out depending upon the availability of material. It was started in 1907, before the establishment of the ZSI. After India’s independence, the ZSI has eight issues of the Memoirs of the Indian Museum in two volumes.</p>
			<p>After 1968, the name was changed to Memoirs of the Zoological Survey of India. Since 1947, 31 issues of the Memoirs of the Indian Museum/Memoirs of Zoological Survey of India have been published in eight volumes.</p>
			<p><span class="bld">Occasional Papers</span></p>
			<p>This series is used to publish under a separate cover findings related to particular topics and animal groups, district faunas, checklists and other lengthy taxonomic research papers that cannot be accommodated in the Records of the Zoological Survey of India. Publication of this series began in 1976, and so far 335 numbers have been published.</p>
			<p><span class="bld">Fauna of British India</span> and <span class="bld">Fauna of India</span></p>
			<p>91 volumes of ‘Fauna of British India’ on different groups were published before independence. This programme was reoriented in 1975 as ‘Fauna of India’. Since then 52 volumes have been published.</p>
			<p>Under this Series, a consolidated and up-to-date Taxonomic/systematic account of different groups of animals, based on detailed studies, are undertaken by the Scientists of eminence, who have worked on that group for more than 2 decades. The fauna volumes provide identification keys and distribution ranges of the species and genera belonging to a particular animal group. These volumes not only provide an inventory of the various taxa of that animal group within Indian region but also their distribution in whole of Asia and the World.</p>
			<p><span class="bld">State Fauna Series</span></p>
			<p>ZSI headquarters in collaboration with its 16 Regional Centres located in various parts of the country has undertaken intensive and extensive survey programmes on the faunal resources of various States of India and collected and documented the baseline data. The first of the series: State Fauna of Orissa was published in 1987. These published documents on fauna have been realised to be highly useful not only to the research workers but also to the state governments to assess/monitor the faunal wealth of their states. The faunal documents also help in EIA works/study as the base line data. The State Fauna series of the 20 States have been published; survey, study and documentation of the fauna of other 4 states are being completed; and the Survey and faunal inventorying works of 3 states are in progress.</p>
			<p><span class="bld">Conservation Area Series</span></p>
			<p>This series of publication was started to survey, study and document the fauna of reserve forests of our country in order to suggest the conservation measures based on the monitoring and inventorying works. The Fauna of Nanda Devi Biosphere Reserve was the first published in the series in 1987. So far 44 publications have been brought out under this series.</p>
			<p><span class="bld">Ecosystem Series</span></p>
			<p>The series was started in the year 1992 to study the different ecosystems of the country viz. Estuarines, Wetlands, Himalaya, Marine, etc. The survey/ exploration, study and documentation of the faunal resources of many more ecosystems are yet to be undertaken under this programme. A total of 23 publications have been published as listed below:</p>
			<ul class="lower_alpha">
				<li>A) Estuarine Ecosystem Series (First Published in 1992); 6 books</li>
				<li>B) Wetland Ecosystem Series (First Published in 1995); 14 books</li>
				<li>C) Himalayan Ecosystem Series (First Published in 1995); 2 books</li>
				<li>D) Marine Ecosystem Series (First Published in 2007); 1 book</li>
			</ul>
			<p><span class="bld">Handbook and Pictorial Guides</span></p>
			<p>The ‘Handbook series’ publications are identification manuals of major groups of animals with keys and illustrations. The handbooks are important tools to help in distinguishing and identifying the species of different animal groups. Pictorial handbooks with colour photographs were also published for common people to generate interest towards fauna of the country. The first handbook on Indian Thysanoptera was published in 1980. So far 48 handbooks have been brought out.</p>
			<p><span class="bld">Special Publications</span></p>
			<p>ZSI has been publishing special publications on proceedings of symposia, seminars and workshops, etc., conducted by the Survey, and books of important and valued nature. The publishing of special publications was started with the first book ‘State of Art report – Zoology’ brought ou in 1980. 45 publications have so far been published under this series, some of which are as important as the ones like: Faunal resources of India, Red Data Books, Environmental Awareness, Ecosystems of India, Geo Spatial Atlas of Birds of Rajasthan, etc.</p>
			<p><span class="bld">Status Survey of Endangered Species</span></p>
			<p>The publication of this series was started with an aim to survey and monitor the status of critically endangered species and threatened species of India. So far 11 documents on status of animals have been published.</p>
			<p><span class="bld">Technical Monographs</span></p>
			<p>Transfer of the Survey to the Department of Environment made it necessary for the activities to be more elaborative, with the inclusion of the applied nature of works also, and the results are published under this series. Its publishing was started in the year 1978, and was discontinued in the year 1987 during which 17 monographs were published.</p>
			<p><span class="bld">Zoologiana</span></p>
			<p>It is a semi-popular journal for publishing accounts of the general nature of faunal resources, conservation of wildlife, etc., on the basis of work done at the Survey. This journal was started in the year 1978 and, after publishing 5 volumes, was discontinued in the year 1990.</p>
			<p><span class="bld">Bulletin of Zoological Survey of India</span></p>
			<p>This journal started in the year 1978 as a house journal was meant to cater to the growing diversity in zoological research undertaken in the Survey. The journal provided the researchers opportunities to publish the short-length papers, or communications of the research findings, especially to facilitate the quick and easy publication of new taxa, records and other findings.  Till it was discontinued in the year 1987, 8 volumes, each in three parts, were published. </p>
