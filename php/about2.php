			<p class="h1">Introduction with Progressive History</p>
			<p>The Library acquired the documents on zoological researches of more than a century old from the former museum (1814-1875) of the Asiatic Society of Bengal and the Zoological Section of the Indian Museum (1875-1916) in Calcutta. The Library of Zoological Survey of India started its journey on 1st. July, 1916 with 12000 volumes of documents transferred from Indian Museum. Mr. C. O. Bateman was its first Librarian. During 2nd World war, the library was shifted from Calcutta to Banaras on 11th  May, 1942. Due to Varuna Flood in 1943, the library was completely disorganized and virtually shattered.  But due to remarkable and efficient supervision of late Shri Samarendra Nath Ghosal, Ex-Librarian, the library was set in order and the books were reconditioned in record time. In early 1949, the library was again shifted and rehoused in the Indian Museum. In 1950, the personal collection of Dr. G. D. Bhalerao was added to the library which was a notable acquisition during that period. The Library of Z.S.I. functioned from 1949 to 2000 in Indian Museum. In the year 2000, the total collections of the library were transferred from Indian Museum to the new Annexed Building of Z.S.I. at New Alipur.</p>
			<p>The library holdings both at the Headquarter Library, Kolkata, and 16 Regional Centers located in different part of India have a total collection of approximately 1,35,519 nos., which includes Books, Bound and Unbound journals, Monographs, Reports of Expeditions and faunal surveys, documents on non-book materials, etc.. The library is having more than 400 titles of Rare Books starting from the year 1547. The holdings have unique collections of books, periodicals and other archaic literature on the particular subject--Zoology and related animal sciences—and form perhaps the biggest library on Animal Sciences in Asia. Even the original publications of Carl Linnaeus and Fabricius, the monumental documents of animal taxonomy, original paintings and drawings of world famous Scientists, biogeographers and Naturalists have enriched the library collections. The rare collections of the Library are in fact the treasures of this institution as well as the whole scientific community of this planet.</p>
